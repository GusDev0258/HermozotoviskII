    public void populaLista(){
        for(Produto prod: pDAO.produtos){
            taProdutos.append("| COD : " + prod.getCodigo() + " | NOME: " + prod.getNome() + " | PRECO: R$" + prod.getPreco());
            taProdutos.append("\n");
        }
    }
